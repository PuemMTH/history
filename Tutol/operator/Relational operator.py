a,b = 5,5
print("a :",a,"b :",b)
print('Relational operator "==" :',a==b)
print('Relational operator "!=" :',a!=b)
print('Relational operator ">" :',a>b)
print('Relational operator "<" :',a<b)
print('Relational operator ">=" :',a>=b)
print('Relational operator "<=" :',a<=b)
# a : 5 b : 5
# Relational operator "==" : True
# Relational operator "!=" : False
# Relational operator ">" : False
# Relational operator "<" : False
# Relational operator ">=" : True
# Relational operator "<=" : True