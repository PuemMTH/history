a,b = 7,5
print("a :",a,"b :",b)
print('Arthmetic operator "+" :',a+b,",plus") # บวก
print('Arthmetic operator "-" :',a-b,",delete") # ลบ
print('Arthmetic operator "*" :',a*b,",multiply") # คูณ
print('Arthmetic operator "/" :',a/b,",Divide the numerator") # หารเอาเศษ
print('Arthmetic operator "//" :',a//b,",Divided by not taking the numerator") # หารไม่เอาเศษ
print('Arthmetic operator "%" :',a%b,",Dividing only the numerator") # หารเอาเฉพาะเศษ
print('Arthmetic operator "**" :',a**b,",Exponent") # ยกกำลัง